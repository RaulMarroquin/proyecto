private double convertirAraidanes(double grados){
    return grados * Math.PI/180;
    
    
}

 private double factorial (double numero){
     
     if(numero == 0 || numero ==1){
        return 1; 
        
     }
     else{
         return numero*factorial (numero-1);
     }
 }

